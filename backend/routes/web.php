use Illuminate\Support\Facades\Event;
use Illuminate\Support\Facades\Route;

Route::get('/up', function () {
    try {
        Event::dispatch(new DiagnosingHealth);
    } catch (Throwable $exception) {
        report($exception);

        return response()->json([
            'status' => 'error',
        ], 500);
    }

    return response()->noContent();
});

