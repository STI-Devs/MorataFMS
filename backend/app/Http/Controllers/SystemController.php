    /**
     * GET /up
     */
    public function health(): Response|JsonResponse
    {
        try {
            Event::dispatch(new DiagnosingHealth);
        } catch (Throwable $exception) {
            report($exception);

            return response()->json([
                'status' => 'error',
            ], 500);
        }

        return response()->noContent();
    }

